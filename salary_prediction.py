# Initialize and train the model
# Using a linear kernel for faster computation, though RBF is common
svr_model = SVR(kernel='linear') # You might experiment with 'rbf' and C, epsilon parameters
svr_model.fit(X_train_scaled, y_train_scaled)

# Make predictions on scaled data
y_pred_svr_scaled = svr_model.predict(X_test_scaled)

# Inverse transform the predictions to the original scale
y_pred_svr = scaler_y.inverse_transform(y_pred_svr_scaled.reshape(-1, 1)).flatten()

# Evaluate the model
mae_svr = mean_absolute_error(y_test, y_pred_svr)
r2_svr = r2_score(y_test, y_pred_svr)

print(f'SVR Mean Absolute Error: {mae_svr:.2f}')
print(f'SVR R-squared: {r2_svr:.2f}')

from sklearn.naive_bayes import GaussianNB
import numpy as np
# Gaussian Naive Bayes is typically for classification.
# To use it for regression, we can discretize the target variable,
# or predict the mean of the distribution for the predicted class.
# For simplicity, we'll demonstrate a common workaround by creating 'bins' for salary.

# It's important to note that this is not a direct regression model
# and results should be interpreted with caution.

# Create bins for the target variabale 'Salary'
y_train_binned = pd.cut(y_train, bins=5, labels=False, include_lowest=True)

# Initialize and train the Gaussian Naive Bayes classifier
gnb_model = GaussianNB()
gnb_model.fit(X_train, y_train_binned)

# Predict probabilities for each bin
y_pred_proba = gnb_model.predict_proba(X_test)

# To get a 'regression' like prediction, we can take the weighted average
# of the bin centers based on predicted probabilities.
# First, define the bin edges to get bin centers.
bins_edges = pd.cut(y_train, bins=5, include_lowest=True, retbins=True)[1]
bin_centers = (bins_edges[:-1] + bins_edges[1:]) / 2

# Select only the bin centers that correspond to the classes learned by the model
y_pred_gnb = np.array([np.sum(prob * bin_centers[gnb_model.classes_]) for prob in y_pred_proba])

# Evaluate the model
mae_gnb = mean_absolute_error(y_test, y_pred_gnb)
r2_gnb = r2_score(y_test, y_pred_gnb)

print(f'Gaussian Naive Bayes (Regression-like) Mean Absolute Error: {mae_gnb:.2f}')
print(f'Gaussian Naive Bayes (Regression-like) R-squared: {r2_gnb:.2f}')

